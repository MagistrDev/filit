#include "libft.h"

void	ft_strclr(char *s)
{
	char	*str;
	size_t	i;

	str = (char*)s;
	i = 0;
	if (str)
	{
		while (str[i])
		{
			str[i] = '\0';
			i++;
		}
	}
}
